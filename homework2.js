// Отримуємо рік від користувача через prompt
let year = prompt("Введіть кількість років:");

// Перевіряємо, чи користувач ввів число і чи воно не від'ємне
if (!isNaN(year) && year >= 0) {
    // Визначаємо форму слова "рік" за останньою цифрою числа
    if (year % 10 === 1 && year % 100 !== 11) {
        alert(year + " рік");
    } else if (year % 10 >= 2 && year % 10 <= 4 && (year % 100 < 10 || year % 100 >= 20)) {
        alert(year + " роки");
    } else {
        alert(year + " років");
    }
} else {
    // Якщо введене значення не валідне, виводимо повідомлення про помилку
    alert("Будь ласка, введіть додатне число.");
}