function pow(x, y) {
    let result = 1;
    // Цикл для возведения числа в степень
    for (let i = 0; i < y; i++) {
        result *= x;
    }
    // Возвращаем результат
    return result;
}
// Пример использования функции
console.log(pow(2, 3)); // Выведет 8
