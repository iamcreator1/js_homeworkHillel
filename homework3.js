let sum = 0;
// Цикл для перебора чисел от 1 до 100
for (let i = 1; i <= 100; i++) {
    // Проверка, не кратно ли текущее число 3
    if (i % 3 !== 0) {
        // Если не кратно, добавляем его к сумме
        sum += i;
    }
}
console.log("Сумма чисел от 1 до 100, не кратных 3, равна:", sum);


