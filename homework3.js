function drawTriangle(height, symbol) {
    // Написал цикл для того, чтоб перебрать сроку
    for (let i = 1; i <= height; i++) {
        // Создание строки для текущего ряда
        let row = '';
        // Добавление символов в строку в количестве текущего номера строки
        for (let j = 1; j <= i; j++) {
            row += symbol;
        }
        console.log(row);
    }
}
drawTriangle(5, '*');


