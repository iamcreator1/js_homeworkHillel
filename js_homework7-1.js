// Рядок для перевірки
const str = "SomeTextWithoutA";

// Регулярний вираз для пошуку рядків, які мають принаймні 6 символів і не містять літеру "A" (велику або малу)
const regex = /^(?!.*[Aa]).{6,}$/;

// Перевірка за допомогою регулярного виразу
if (regex.test(str)) {
    console.log("Рядок відповідає умовам.");
} else {
    console.log("Рядок не відповідає умовам.");
}
